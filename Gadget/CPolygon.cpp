/*---------------------------------------------------------------------+\

 * Render -

\+---------------------------------------------------------------------*/
bool	CPolygon::Render
		(
		VDictionaryPtr pDict,
		VDrawPtr pDraw
		)
{
	bool	bResult = VPolylist::Render( pDict, pDraw );

	bResult = pDraw->BeginPolygon();
	if ( bResult )
	{
		bResult = VPolylist::Render( pDict, pDraw );
		if ( bResult )
			bResult = pDraw->EndPolygon();
	}

	return bResult;
}



