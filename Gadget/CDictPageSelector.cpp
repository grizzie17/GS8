/*----------------------------------------------------------------------+\

 * operator= - assignment operator

\+---------------------------------------------------------------------*/
CDictPageSelectorRef
		CDictPageSelector::operator=
		(
		ConstCDictPageSelectorRef	r
		)
{
	m_nSelector = ~0UL;
	m_tName = r.m_tName;

	return *this;
}


/*---------------------------------------------------------------------+\

 * SetName -

\+---------------------------------------------------------------------*/
void	CDictPageSelector::SetName
		(
		const char *sName
		)
{
	m_tName = sName;
	m_nSelector = ~0UL;
}


/*---------------------------------------------------------------------+\

 * SetName -

\+---------------------------------------------------------------------*/
void	CDictPageSelector::SetName
		(
		ConstCCharDescriptorRef rName
		)
{
	m_tName = rName;
	m_nSelector = ~0UL;
}


/*---------------------------------------------------------------------+\

 * Name -

\+---------------------------------------------------------------------*/
CCharStringPtr
CDictPageSelector::Name
		(
		void
		)
{
	return &m_tName;
}


/*---------------------------------------------------------------------+\

 * GetSelector -

\+---------------------------------------------------------------------*/
VDictionary::Selector
		CDictPageSelector::GetSelector
		(
		VDictionaryPagePtr pDictPage
		)
{
	if ( ~0u == m_nSelector )
		m_nSelector = (VDictionary::Selector)pDictPage->LocateEntry( m_tName );
	return m_nSelector;
}

