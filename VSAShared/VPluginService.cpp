/*---------------------------------------------------------------------+\
|	 IService
\+---------------------------------------------------------------------*/

/*---------------------------------------------------------------------+\

 * Start -

\+---------------------------------------------------------------------*/
NResult	VPluginService::Start
		(
		void
		)
{
	NResult	nr = NR_E_NOTHREAD;

	m_bRunning = false;

	return nr;
}

/*---------------------------------------------------------------------+\

 * Stop -

\+---------------------------------------------------------------------*/
NResult	VPluginService::Stop
		(
		void
		)
{

	m_bRunning = false;

	return NR_S_SUCCESS;
}

/*---------------------------------------------------------------------+\

 * IsRunning -

\+---------------------------------------------------------------------*/
bool	VPluginService::IsRunning
		(
		void
		)
{
	CMutexLocker	lock( &m_oMutex );

	return m_bRunning;
}


/*---------------------------------------------------------------------+\

 * GetID -

\+---------------------------------------------------------------------*/
ConstCCharStringRef
		VPluginService::GetID
		(
		void
		)
{
	return m_sID;
}



/*----------------------------------------------------------------------+\
|	IPluginLoadConfiguration
\+---------------------------------------------------------------------*/
/*---------------------------------------------------------------------+\

 * Load -

\+---------------------------------------------------------------------*/
bool	VPluginService::Load
		(
		VPluginConfigurationPtr	pConfig,
		VPluginLibraryPtr		pLib
		)
{
	m_sID = pConfig->GetID();
	return inherited::Load( pConfig, pLib );
}


