/*---------------------------------------------------------------------+\

 * FindInternalInterface -

\+---------------------------------------------------------------------*/
void*	VPluginService::FindInternalInterface
		(
		ConstIXIDRef rIID
		)
{
	void*	p = inherited::FindInternalInterface( rIID );
	if ( p )
		return p;

	if ( rIID == IXID_IService )
		return INTERFACE_THIS( IServicePtr );
	else
		return 0;
}


