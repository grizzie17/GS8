/*---------------------------------------------------------------------+\

 * ConvertLowercase -

\+---------------------------------------------------------------------*/
void	CCharDescriptor::ConvertLowercase
		(
		void
		)
{
	char*	p = m_s;
	char*	pEnd = p + m_n;
	while ( p < pEnd )
	{
		(*p) = static_cast<char>(::tolower( *p ));
		++p;
	}
}


/*---------------------------------------------------------------------+\

 * ConvertUppercase -

\+---------------------------------------------------------------------*/
void	CCharDescriptor::ConvertUppercase
		(
		void
		)
{
	char*	p = m_s;
	char*	pEnd = p + m_n;
	while ( p < pEnd )
	{
		(*p) = static_cast<char>(::toupper( *p ));
		++p;
	}
}


